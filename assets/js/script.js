let currentLang = 'pt';

/* ========================================
   Language Switching
   ======================================== */
function setLanguage(lang) {
    currentLang = lang;
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    document.getElementById('btn-pt').classList.toggle('active', lang === 'pt');
    document.getElementById('btn-en').classList.toggle('active', lang === 'en');
    document.documentElement.lang = lang === 'pt' ? 'pt-br' : 'en';
    localStorage.setItem('lang', lang);

    // Update expand/collapse button text for open cards
    document.querySelectorAll('.card-expand').forEach(btn => {
        const isExpanded = btn.getAttribute('aria-expanded') === 'true';
        const spanEl = btn.querySelector('span');
        if (spanEl) {
            spanEl.textContent = isExpanded
                ? translations[lang]['card-less']
                : translations[lang]['card-more'];
        }
    });
}

/* ========================================
   Scroll Reveal
   ======================================== */
function initScrollReveal() {
    const sections = document.querySelectorAll('.section');
    const observer = new IntersectionObserver(
        (entries) => {
            entries.forEach((entry) => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('revealed');
                }
            });
        },
        { threshold: 0.1, rootMargin: '0px 0px -40px 0px' }
    );
    sections.forEach((section) => observer.observe(section));
}

/* ========================================
   Navbar scroll behavior
   ======================================== */
function initNavbarScroll() {
    const navbar = document.getElementById('navbar');
    let lastScroll = 0;

    window.addEventListener('scroll', () => {
        const currentScroll = window.scrollY;
        navbar.classList.toggle('scrolled', currentScroll > 20);
        lastScroll = currentScroll;
    }, { passive: true });
}

/* ========================================
   Mobile Menu
   ======================================== */
function initMobileMenu() {
    const toggle = document.getElementById('menu-toggle');
    const navLinks = document.getElementById('nav-links');

    // Create overlay
    const overlay = document.createElement('div');
    overlay.className = 'nav-overlay';
    document.body.appendChild(overlay);

    function openMenu() {
        toggle.classList.add('active');
        navLinks.classList.add('open');
        overlay.classList.add('visible');
        document.body.style.overflow = 'hidden';
    }

    function closeMenu() {
        toggle.classList.remove('active');
        navLinks.classList.remove('open');
        overlay.classList.remove('visible');
        document.body.style.overflow = '';
    }

    toggle.addEventListener('click', () => {
        const isOpen = navLinks.classList.contains('open');
        isOpen ? closeMenu() : openMenu();
    });

    overlay.addEventListener('click', closeMenu);

    // Close on link click
    navLinks.querySelectorAll('a').forEach(link => {
        link.addEventListener('click', closeMenu);
    });
}

/* ========================================
   Expandable Cards
   ======================================== */
function initExpandableCards() {
    document.querySelectorAll('.card-expand').forEach(btn => {
        btn.addEventListener('click', () => {
            const card = btn.closest('.expertise-card');
            const details = card.querySelector('.card-details');
            const isExpanded = btn.getAttribute('aria-expanded') === 'true';
            const spanEl = btn.querySelector('span');

            if (isExpanded) {
                details.classList.remove('open');
                btn.setAttribute('aria-expanded', 'false');
                if (spanEl) spanEl.textContent = translations[currentLang]['card-more'];
            } else {
                details.classList.add('open');
                btn.setAttribute('aria-expanded', 'true');
                if (spanEl) spanEl.textContent = translations[currentLang]['card-less'];
            }
        });
    });
}

/* ========================================
   Equalize card heights
   ======================================== */
function initEqualCards() {
    const grid = document.querySelector('.expertise-grid');
    if (!grid) return;

    function equalize() {
        const cards = grid.querySelectorAll('.expertise-card');
        const summaries = grid.querySelectorAll('.card-summary');

        // Reset to measure natural heights
        cards.forEach(c => c.style.minHeight = '');
        summaries.forEach(s => s.style.minHeight = '');

        // Only equalize on 2-column layout (> 768px)
        if (window.innerWidth <= 768) return;

        let maxH = 0;
        cards.forEach(c => {
            if (c.offsetHeight > maxH) maxH = c.offsetHeight;
        });
        cards.forEach(c => c.style.minHeight = maxH + 'px');

        // Lock each summary's rendered height so it won't shrink when details open
        summaries.forEach(s => {
            s.style.minHeight = s.offsetHeight + 'px';
        });
    }

    equalize();
    window.addEventListener('resize', equalize);
}

/* ========================================
   Email obfuscation
   ======================================== */
function initEmailObfuscation() {
    const card = document.getElementById('email-card');
    if (!card) return;

    const u = card.dataset.u;
    const d = card.dataset.d;
    const addr = u + '@' + d;

    card.href = 'mail' + 'to:' + addr;
    document.getElementById('email-display').textContent = addr;
}

/* ========================================
   Theme Toggle
   ======================================== */
function initThemeToggle() {
    const toggle = document.getElementById('theme-toggle');
    if (!toggle) return;

    const savedTheme = localStorage.getItem('theme') || 'dark';
    document.documentElement.setAttribute('data-theme', savedTheme);

    toggle.addEventListener('click', () => {
        const current = document.documentElement.getAttribute('data-theme') || 'dark';
        const next = current === 'dark' ? 'light' : 'dark';
        document.documentElement.setAttribute('data-theme', next);
        localStorage.setItem('theme', next);
    });
}

/* ========================================
   Smooth scroll for anchor links
   ======================================== */
function initSmoothScroll() {
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', (e) => {
            const target = document.querySelector(anchor.getAttribute('href'));
            if (target) {
                e.preventDefault();
                target.scrollIntoView({ behavior: 'smooth' });
            }
        });
    });
}

/* ========================================
   Init
   ======================================== */
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('lang') || 'pt';
    setLanguage(savedLang);

    document.getElementById('btn-pt').addEventListener('click', () => setLanguage('pt'));
    document.getElementById('btn-en').addEventListener('click', () => setLanguage('en'));

    initThemeToggle();
    initScrollReveal();
    initNavbarScroll();
    initMobileMenu();
    initExpandableCards();
    initEqualCards();
    initEmailObfuscation();
    initSmoothScroll();
});
